"use client";

import { useEffect, useState } from "react";
import { motion, AnimatePresence } from "framer-motion";
import { X, ShieldCheck, FileText } from "lucide-react";

export default function LegalModals() {
  const [activeModal, setActiveModal] = useState<"policy" | "offer" | null>(null);

  useEffect(() => {
    const openPolicy = () => setActiveModal("policy");
    const openOffer = () => setActiveModal("offer");
    
    window.addEventListener("open-privacy-policy", openPolicy);
    window.addEventListener("open-public-offer", openOffer);
    
    return () => {
      window.removeEventListener("open-privacy-policy", openPolicy);
      window.removeEventListener("open-public-offer", openOffer);
    };
  }, []);

  const closeModal = () => setActiveModal(null);

  useEffect(() => {
    document.body.style.overflow = activeModal ? 'hidden' : 'unset';
  }, [activeModal]);

  return (
    <AnimatePresence>
      {activeModal && (
        <div className="fixed inset-0 z-[150] flex items-center justify-center p-4">
          
          {/* Backdrop */}
          <motion.div
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            exit={{ opacity: 0 }}
            onClick={closeModal}
            className="absolute inset-0 bg-black/90 backdrop-blur-md"
          />

          {/* Modal Container */}
          <motion.div
            initial={{ opacity: 0, scale: 0.95, y: 20 }}
            animate={{ opacity: 1, scale: 1, y: 0 }}
            exit={{ opacity: 0, scale: 0.95, y: 20 }}
            className="relative w-full max-w-[900px] bg-[#141414] border border-white/10 rounded-2xl overflow-hidden shadow-2xl flex flex-col max-h-[85vh]"
          >
            {/* Header */}
            <div className="flex items-center justify-between p-6 border-b border-white/5 bg-[#1a1a1a]">
               <div className="flex items-center gap-3">
                  <div className="p-2 rounded-lg bg-white/5 text-[#FF2E63]">
                    {activeModal === 'policy' ? <ShieldCheck size={24} /> : <FileText size={24} />}
                  </div>
                  <h3 className="font-chakra font-bold text-xl text-white uppercase tracking-wide">
                    {activeModal === 'policy' ? "Политика конфиденциальности" : "Публичная оферта"}
                  </h3>
               </div>
               <button 
                 onClick={closeModal}
                 className="p-2 rounded-full hover:bg-white/10 text-gray-400 hover:text-white transition-colors"
               >
                 <X size={24} />
               </button>
            </div>

            {/* Scrollable Content */}
            <div className="flex-1 overflow-y-auto p-6 md:p-10 text-gray-300 font-inter text-sm leading-relaxed space-y-6 custom-scrollbar">
               {activeModal === 'policy' ? <PrivacyPolicyText /> : <PublicOfferText />}
            </div>

            {/* Footer */}
            <div className="p-4 border-t border-white/5 bg-[#1a1a1a] flex justify-end">
               <button 
                 onClick={closeModal}
                 className="px-6 py-2 bg-white text-black font-chakra font-bold text-sm uppercase rounded-lg hover:bg-[#FF2E63] hover:text-white transition-colors"
               >
                 Закрыть
               </button>
            </div>

          </motion.div>
        </div>
      )}
    </AnimatePresence>
  );
}

