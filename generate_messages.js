// Пример генерации случайного сообщения:
function generateMessage() {
    const prefix = prefixes[Math.floor(Math.random() * prefixes.length)];
    const body = bodies[Math.floor(Math.random() * bodies.length)];
    const advice = advices[Math.floor(Math.random() * advices.length)];
    const suffix = suffixes[Math.floor(Math.random() * suffixes.length)];

    return `${prefix} ${body} ${advice} ${suffix}`;
}

function getRandom(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

const messages = new Set();
while (messages.size < 190) {
    const p = getRandom(prefixes);
    const b = getRandom(bodies);
    const a = getRandom(advices);
    const s = getRandom(suffixes);
    const message = `${p} ${b} ${a} ${s}`.trim();
    messages.add(message);
}

const fileContent = `// Automatically generated welcome messages
export const WELCOME_MESSAGES: string[] = [
${Array.from(messages).map(m => `    "${m}"`).join(',\n')}
];
`;

fs.writeFileSync('lib/welcome-messages.ts', fileContent, 'utf-8');
console.log('Successfully generated 190 messages to lib/welcome-messages.ts');
